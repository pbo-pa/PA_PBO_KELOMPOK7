    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        String id = iddokter.getText();
        String nama = namadokter.getText();
        String spesialisasi = spesialisasidokter.getText();
        String telepon = notelepon.getText();
        String jadwalKerja = jadwal.getText();
        String idPelayanan = null; 

        if (id.isEmpty() || nama.isEmpty() || spesialisasi.isEmpty() || telepon.isEmpty() || jadwalKerja.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DDokter dokter = new DDokter(id, nama, spesialisasi, telepon, jadwalKerja, idPelayanan);

        try {
            dokter.simpan();
            JOptionPane.showMessageDialog(this, "Data dokter berhasil ditambahkan.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadTableData(); 
            resetform(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }    
    private void resetform() {
        iddokter.setText("");
        namadokter.setText("");
        spesialisasidokter.setText("");
        notelepon.setText("");
        jadwal.setText("");
    }//GEN-LAST:event_tambahActionPerformed

