    private void loadTableData() {
        try {
            List<DDokter> daftarDokter = DDokter.viewTampilanSemua();

            DefaultTableModel tableModel = (DefaultTableModel) jTableupdate.getModel();

            tableModel.setRowCount(0);

            for (DDokter dokter : daftarDokter) {
                tableModel.addRow(new Object[]{
                        dokter.getIdDokter(),        
                        dokter.getNamaDokter(),      
                        dokter.getSpesialisasi(),    
                        dokter.getTelepon(),        
                        dokter.getJadwalKerja(),     
                        dokter.getIdPelayanan()      
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    
