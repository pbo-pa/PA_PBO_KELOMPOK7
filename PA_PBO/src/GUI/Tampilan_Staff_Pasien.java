    private void jTableupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableupdateMouseClicked
        int selectedRow = jTableupdate.getSelectedRow(); 
    
        if (selectedRow >= 0) { 
            DefaultTableModel tableModel = (DefaultTableModel) jTableupdate.getModel();

            id_pasien.setText(tableModel.getValueAt(selectedRow, 0).toString()); 
            nama_pasien.setText(tableModel.getValueAt(selectedRow, 1).toString()); 
            no_bpjs.setText(tableModel.getValueAt(selectedRow, 2).toString()); 
            no_telepon.setText(tableModel.getValueAt(selectedRow, 3).toString()); 
            Status_antrian.setSelectedItem(tableModel.getValueAt(selectedRow, 4).toString()); 
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada baris yang dipilih!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTableupdateMouseClicked

