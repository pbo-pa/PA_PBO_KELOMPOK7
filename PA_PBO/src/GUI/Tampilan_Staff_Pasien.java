    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        String idPasien = id_pasien.getText();
        String nama = nama_pasien.getText();
        String noBpjs = no_bpjs.getText();
        String telepon = no_telepon.getText();
        String statusAntrian = Status_antrian.getSelectedItem().toString();
        String idPenyakit = null; 

        if (idPasien.isEmpty() || nama.isEmpty() || noBpjs.isEmpty() || telepon.isEmpty() || statusAntrian.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DataPasien pasien = new DataPasien(idPasien, nama, noBpjs, telepon, statusAntrian, idPenyakit);

        try {
            pasien.simpan(); 
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadTableData(); 
            resetForm(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }  

    private void resetForm() {
        id_pasien.setText(""); 
        nama_pasien.setText(""); 
        no_bpjs.setText(""); 
        no_telepon.setText(""); 
        Status_antrian.setSelectedIndex(0); 
    }//GEN-LAST:event_tambahActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String idPasien = id_pasien.getText();
        String nama = nama_pasien.getText();
        String noBpjs = no_bpjs.getText();
        String telepon = no_telepon.getText();
        String statusAntrian = Status_antrian.getSelectedItem().toString();

        DataPasien pasien = new DataPasien(idPasien, nama, noBpjs, telepon, statusAntrian, null);

        try {
            pasien.perbarui();
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadTableData();
            resetForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateActionPerformed

