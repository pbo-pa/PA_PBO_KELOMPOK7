    private void loadTableData() {
    try {
        List<DataPasien> daftarPasien = DataPasien.viewTampilanSemua();

        DefaultTableModel tableModel = (DefaultTableModel) jTableupdate.getModel();

        tableModel.setRowCount(0);

        for (DataPasien pasien : daftarPasien) {
            tableModel.addRow(new Object[]{
                    pasien.getIdPasien(),
                    pasien.getNama(),
                    pasien.getNoBpjs(),
                    pasien.getTelepon(),
                    pasien.getStatusAntrian()
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
